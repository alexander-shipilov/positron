/**
 * The {@link NUMBER_TYPE} constant represents the name of the `number` type
 * that the `typeof` operator returns.
 *
 * @public
 */
export const NUMBER_TYPE = "number";

/**
 * The {@link NumberType} literal string type represents the name of the
 * `number` type that the `typeof` operator returns.
 *
 * @public
 */
export type NumberType = typeof NUMBER_TYPE;
