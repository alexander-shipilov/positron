/**
 * The {@link UNDEFINED_TYPE} constant represents the name of the `undefined`
 * type that the `typeof` operator returns.
 *
 * @public
 */
export const UNDEFINED_TYPE = "undefined";

/**
 * The {@link UndefinedType} literal string type represents the name of the
 * `undefined` type that the `typeof` operator returns.
 *
 * @public
 */
export type UndefinedType = typeof UNDEFINED_TYPE;
